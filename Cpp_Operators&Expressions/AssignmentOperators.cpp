#include<iostream>
using namespace std;
main()
{
//Assignment operator : "="
	int n=20;
	cout<<"Value of n : "<<n<<endl;
	
//Compound Assignment operators : "+=,-=,*=,/=,%="
	n+=10;
	cout<<"Value of n : "<<n<<endl;
	n-=5;
	cout<<"Value of n : "<<n<<endl;
	n*=8;
	cout<<"Value of n : "<<n<<endl;
	n/=10;
	cout<<"Value of n : "<<n<<endl;
	n%=10;
	
	
}
